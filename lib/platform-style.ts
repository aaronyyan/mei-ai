export function buildPlatformStylePrompt(text: string) {
  const normalized = text.toLowerCase();

  if (/小红书|xhs|rednote/.test(normalized)) {
    return [
      '如果用户明确要“小红书风格”，则必须严格按小红书笔记文案的风格输出，不要退回普通广告稿写法。',
      '小红书风格是硬约束，不是参考项。',
      '标题要求：更像真实博主会发的种草标题，要有情绪感、结果感或反差感，可以带 1-2 个自然 emoji。',
      '正文要求：必须更口语化、更像分享，不要像品牌官宣，不要像海报主文案，不要像医疗机构宣传页。',
      '排版要求：正文分成 3-6 个短段落，每段 1-2 句，明显分行，阅读节奏轻快。',
      'emoji 要求：正文可自然使用 3-6 个 emoji，分散出现，用来强化语气、重点和停顿；不能一整篇完全没有 emoji。',
      '语气要求：像真实用户经验分享 + 种草推荐，允许有轻微感叹和互动感，但不要夸张到像低质微商。',
      '表达要求：多用“真的”“其实”“我更喜欢这种”“做完会有那种”这类小红书常见口吻，但仍保持高级和可信。',
      '结尾要求：更像笔记收尾，可以有一句轻互动或总结句，但不要出现“私信我”“到院面诊”“立即预约”。',
      '输出格式仍然只保留标题和正文两部分，但标题和正文内部风格必须完整符合小红书语境。',
    ].join('\n');
  }

  if (/朋友圈|微信朋友圈/.test(normalized)) {
    return [
      '如果用户明确要“朋友圈风格”，输出更适合熟人社交语境。',
      '朋友圈风格要求：语言自然、克制、可信，不要过度营销，不要 emoji 泛滥。',
    ].join('\n');
  }

  if (/instagram|ins\b/.test(normalized)) {
    return [
      '如果用户明确要 Instagram 风格，语言更轻盈、更国际化，排版可更短句。',
      '可少量使用 emoji，但仍保持高级感和克制感。',
    ].join('\n');
  }

  return '';
}
